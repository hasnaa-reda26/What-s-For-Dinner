var ingredientsContainer = document.getElementById('ingredientsContainer');
var instructionsContainer = document.getElementById('instructionsContainer');
var tipsContainer = document.getElementById('tipsContainer');
var prepWarning = document.getElementById('prepWarning');

function displayRecipe(nextRecipe) {
    
    recipeTitle.innerText = nextRecipe.title;
    recipeDescription.innerText = nextRecipe.description;
    recipeImg.src = nextRecipe.image;
    recipeRate.innerText = nextRecipe.rate;
    recipeReviews.innerText = nextRecipe.reviews;
    difficulty.innerText = nextRecipe.difficulty;
    category.innerText = nextRecipe.category;
    
    prepTime.innerText = nextRecipe.prepTime;
    cookTime.innerText = nextRecipe.cookTime;
    servings.innerText = nextRecipe.servings;
    
    nutrCalories.innerText = nextRecipe.calories;
    nutrProtein.innerText = nextRecipe.protein;
    nutrCarbs.innerText = nextRecipe.carbs;
    nutrFat.innerText = nextRecipe.fat;
    nutrFiber.innerText = nextRecipe.fiber;
    nutrSodium.innerText = nextRecipe.sodium;
    
    ingredientsContainer.innerHTML = "";
    nextRecipe.ingredients.forEach(function(item, index) {
        ingredientsContainer.innerHTML += `
            <div class="d-flex align-items-center gap-3 mb-3">
                <div class="number">${index + 1}</div>
                <span class="fs-6">${item}</span>
            </div>
        `;
    });
    
    instructionsContainer.innerHTML = "";
    nextRecipe.instructions.forEach(function(step, index) {
        instructionsContainer.innerHTML += `
            <div class="d-flex align-items-center gap-3 mb-4">
                <div class="number-Ins fw-bold">${index + 1}</div>
                <span class="fs-6">${step}</span>
            </div>
        `;
    });
    
    tipsContainer.innerHTML = "";
    nextRecipe.tips.forEach(function(tip) {
        tipsContainer.innerHTML += `
            <div class="chef-tip-card d-flex align-items-center gap-3 p-4 border-0 rounded-3">
                <div class="text-orange">
                    <i class="fas fa-check-circle fs-5"></i>
                </div>
                <div class="text-dark fw-medium">${tip}</div>
            </div>
        `;
    });

    if (nextRecipe.showWarning === true) {
        prepWarning.classList.remove('d-none'); 
    } else {
        prepWarning.classList.add('d-none');   
    }
}

